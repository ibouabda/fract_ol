#include "libft.h"

void	ft_3dstrdel(char ***as)
{
	int i;

	i = 0;
	if (as != NULL)
	{
		while (as[i])
		{
			ft_2dstrdel(as[i]);
			i++;
		}
		free(as);
		as = NULL;
	}
}
